## Bitwise Operators

x = 5
y = 10

print(x&y)
print(x|y)
print(x^y)
print(~y)
print(x<<2)
print(x>>2)

## Membership Test Operator

li = [1,5,9,74,34,21,45]

print(5 in li)

print(99 not in li)


# Logical Operators

x,y = 5,6

print( x>1 and y<10)
